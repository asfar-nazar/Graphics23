using System.Windows;
using System.Windows.Media;
using System.Windows.Input;
using System.Windows.Controls;
using System.Windows.Media.Imaging;

namespace A25;

class MyWindow : Window {
   public MyWindow () {
      Width = 900; Height = 600;
      Left = 50; Top = 50;
      WindowStyle = WindowStyle.None;
      mBmp = new GrayBMP (Width, Height);

      Image image = new () {
         Stretch = Stretch.None,
         HorizontalAlignment = HorizontalAlignment.Left,
         VerticalAlignment = VerticalAlignment.Top,
         Source = mBmp.Bitmap
      };
      RenderOptions.SetBitmapScalingMode (image, BitmapScalingMode.NearestNeighbor);
      RenderOptions.SetEdgeMode (image, EdgeMode.Aliased);
      Content = image;


      DrawMandelbrot (-0.5, 0, 1);
   }
   
   void LeafFill (string file) {      
      PolyFill p = new ();
      foreach (var line in File.ReadAllLines (file)) { 
         var items = line.Split (' '); 
         p.AddLine (int.Parse (items[0]), int.Parse (items[1]), int.Parse (items[2]), int.Parse (items[3])); 
      }
      p.Fill (mBmp, 255);
   }

   void DrawMandelbrot (double xc, double yc, double zoom) {
      try {
         mBmp.Begin ();
         int dx = mBmp.Width, dy = mBmp.Height;
         double step = 2.0 / dy / zoom;
         double x1 = xc - step * dx / 2, y1 = yc + step * dy / 2;
         for (int x = 0; x < dx; x++) {
            for (int y = 0; y < dy; y++) {
               Complex c = new (x1 + x * step, y1 - y * step);
               mBmp.SetPixel (x, y, Escape (c));
            }
         }
      } finally {
         mBmp.End ();
      }
   }

   byte Escape (Complex c) {
      Complex z = Complex.Zero;
      for (int i = 1; i < 32; i++) {
         if (z.NormSq > 4) return (byte)(i * 8);
         z = z * z + c;
      }
      return 0;
   }

   GrayBMP mBmp;
}

internal class Program {
   [STAThread]
   static void Main (string[] args) {
      Window w = new MyWindow ();
      w.Show ();
      Application app = new Application ();
      app.Run ();
   }
}
